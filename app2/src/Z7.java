class Z7
{
	public static void main(String[] args) 
	{
		System.out.println(195 | 471); // bitwise or
		System.out.println(195 & 471); // bitwise and
		System.out.println(195 ^ 471); // bitwise xor	
		System.out.println(471 >> 1); // bitwise right shift by one	
		System.out.println(471 >> 2); // bitwise right shift by two
		System.out.println(471 << 1); // bitwise left shift by one
		System.out.println(471 << 2); // bitwise left shift by two
	}
}
